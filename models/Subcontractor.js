import mongoose, { Schema, models } from 'mongoose';

const subcontractorSchema = new Schema({
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
  },
  phone: {
    type: String,
  },
  address: {
    type: String,
  },
  kraPin: {
    type: String,
  },
  specialty: {
    type: String,
  },
  notes: {
    type: String,
  },
  createdAt: {
    type: Date,
    default: Date.now,
  },
  updatedAt: {
    type: Date,
    default: Date.now,
  },
}, { timestamps: true });

export default models.Subcontractor || mongoose.model('Subcontractor', subcontractorSchema);
